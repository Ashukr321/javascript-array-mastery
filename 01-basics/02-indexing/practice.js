// Practice: Array Indexing

const colors = ['Red', 'Green', 'Blue', 'Yellow'];

// 1. Log the first color.
// 2. Log the last color.
// 3. Update the second color to 'Emerald'.
// 4. Log the updated array.

console.log(colors[0]); // Red
console.log(colors[colors.length - 1]); // Yellow
colors[1] = 'Emerald';
console.log(colors); // ['Red', 'Emerald', 'Blue', 'Yellow']
